package com.cc.database.datasource;

public class Defs
{
    private String pos;
    private String def;

    public void setPos(String pos)
    {
        this.pos = pos;
    }
    public void setDef(String def)
    {
        this.def = def;
    }


    public String getPos()
    {
        return pos;
    }
    public String getDef()
    {
        return def;
    }
}